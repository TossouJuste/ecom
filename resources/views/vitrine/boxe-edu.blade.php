								<h3 class="title m-b15 font-weight-700 wow fadeInUp" data-wow-delay="0.5s">La boxe Éducative.</h3>
								<p class="wow fadeInUp" data-wow-delay="1.1s">La Boxe Éducative est la boxe la plus développé a UGBD. c'est une discipline ouverte à tous, dès l'âge de 6 ans et jusqu'à 77 ans, offrant une pratique adaptée à toutes les tranches d'âge. Elle se distingue des formes de boxe amateur et professionnelle par deux principes essentiels qui sont L'absence de tout acte nuisible envers l'adversaire et la sanction immédiate pour toute conduite violente ou contraire à l'esprit de la discipline.</p>
								<p class="wow fadeInUp" data-wow-delay="1.1s">Bien que la logique sous-jacente soit commune à toutes les formes de boxe, c’est la force des coups qui distingue la Boxe Éducative des autres formes. Dans cette discipline, où le contact est léger, la puissance des coups est contrôlée pour garantir la sécurité des participants. Les boxeurs doivent remporter leurs confrontations grâce à leur technique et à leur tactique, jamais par la force brute.</p>
								<p class="wow fadeInUp" data-wow-delay="1.1s"> Le but de la Boxe Éducative est de toucher son adversaire sans se faire toucher, avec une rapidité d'exécution similaire aux autres formes de boxe, mais maîtrisée pour éviter toute sanction. Elle peut être pratiquée comme loisir, sans opposition directe, par ceux qui souhaitent s'entraîner sans participer à des combats réels. Cette approche inclut des exercices variés sur la technique, la gestuelle, la motricité, l'endurance et d'autres compétences, sans confrontation physique.</p>
								<p class="wow fadeInUp" data-wow-delay="1.1s">Pour les novices, la Boxe Éducative offre une opportunité de gagner en confiance avant de passer à des formes plus intenses de la discipline. En compétition, la sécurité est primordiale, l'arbitre veillant strictement au respect des règles. Toute tentative de frapper fort, de viser des zones interdites, de tenir ou de pousser l'adversaire, ou d'adopter une attitude antisportive, peut entraîner une disqualification immédiate.</p>
								<p class="wow fadeInUp" data-wow-delay="1.1s">La Boxe Éducative contribue au développement de la vitesse, de l'endurance, de la souplesse et de l'agilité, tout en enseignant les techniques de base de la boxe, telles que l'attaque, la défense et la contre-attaque. L'entraîneur s'appuie sur l'entraînement et les combats pour enseigner la gestion de l'effort, de l'espace et du temps, augmentant ainsi les chances de succès de l'athlète.</p>
								<p class="wow fadeInUp" data-wow-delay="1.1s">Aspects Sociaux : Depuis environ cinq ans, notre association, L’UGBD, s'engage auprès des jeunes filles et garçons confrontés à diverses difficultés. Notre approche se concentre sur la prévention, l'éducation et l'intégration sociale par le sport. Les programmes de boxe socio-éducative que nous proposons sont spécialement conçus pour répondre aux besoins des jeunes en situation de vulnérabilité, souvent marqués par des expériences de vie difficiles, des ruptures, des échecs et des traumatismes émotionnels, susceptibles de générer des troubles comportementaux.</p>

